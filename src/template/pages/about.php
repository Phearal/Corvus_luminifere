            <h3>Nos valeurs</h3>
            <div class="separator"></div>
            <p>Nous avons à coeur de fournir les meilleures conditions pour l’apprentissage artistique, cela passe par :</p>
            <ul>
                <li>Des ateliers ouverts à tous : Que vous soyez débutant ou confirmé, enfant, adolescent ou adulte, nos cours sont adaptés à tous les niveaux et à tous les âges.</li>
                <li>Une approche conviviale : Nous croyons en l'importance de créer un environnement accueillant où chacun se sent à l'aise pour exprimer sa créativité.</li>
                <li>Des programmes diversifiés : Nos cours de dessin et de peinture sont conçus pour répondre à une variété d'intérêts et de besoins, offrant ainsi une expérience enrichissante à chaque participant.</li>
                <li>Un espace d'échange et de partage : Chez Corvus, nous encourageons les interactions entre les participants, favorisant ainsi l'apprentissage collaboratif et la découverte de nouvelles techniques.</li>
                <li>Une équipe passionnée : Nos instructeurs sont des artistes expérimentés et pédagogues, dévoués à vous guider tout au long de votre parcours artistique et à vous inspirer à chaque étape.</li>
            </ul>
